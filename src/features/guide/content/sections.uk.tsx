export function loadGuideSections(locale: Locale): GuideSection[] {
  if (locale === "uk") return sections;
  // Adding a new locale to the union without extending this switch becomes a
  // compile error here — the unreachable branch is intentional.
  const exhaustive: never = locale;
  throw new Error(`Unsupported guide locale: ${String(exhaustive)}`);
}
